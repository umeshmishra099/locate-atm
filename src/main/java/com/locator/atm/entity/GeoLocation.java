package com.locator.atm.entity;

public class GeoLocation
{
  private String lat;
  private String lng;

  public String getLat()
  {
    return this.lat;
  }

  public void setLat(String lat)
  {
    this.lat = lat;
  }

  public String getLng()
  {
    return this.lng;
  }

  public void setLng(String lng)
  {
    this.lng = lng;
  }
}
